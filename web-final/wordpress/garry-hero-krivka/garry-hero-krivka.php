define( 'GARRY_HK_OPT', 'garry_hk_settings' );

function garry_hk_defaults() {
	return array( 'enabled' => 1, 'thickness' => 3, 'speed' => 2.6 );
}
function garry_hk_get() {
	$o = get_option( GARRY_HK_OPT, array() );
	return wp_parse_args( is_array( $o ) ? $o : array(), garry_hk_defaults() );
}

Garry_Promotion_Registry::register( array(
	'slug' => 'garry-hero-krivka', 'title' => 'Hero křivka trati',
	'callback' => 'garry_hk_admin_page', 'plugin_file' => __FILE__, 'dashicon' => 'dashicons-flag', 'position' => 15,
